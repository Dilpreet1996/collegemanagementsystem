    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int a=Integer.parseInt(mo.getText());
        int b=Integer.parseInt(of.getText());

        if(a>b)
        {
            JOptionPane.showMessageDialog(null, "Please Enter a Valid Marks");
        }
        if(a<=b)
        {
            if(!sub.getSelectedItem().equals("") && !mo.getText().equals("") && !of.getText().equals(""))
            {

                String qry="";
                model = new DefaultTableModel();
                String columnNames[]={"Subjects","Marks Obtained","Out Of"};
                model.setColumnIdentifiers(columnNames);

                String tsub = "";

                String tmo= "";

                String tof= "";
                int idx=sub.getSelectedIndex();
                subj[id]=sub.getSelectedItem().toString();
                id++;

                moj[i1]=mo.getText().toString();
                i1++;
                ofj[i2]=of.getText().toString();
                i2++;
                for(int i=0;i<id;i++){
                    model.addRow(new Object[]{subj[i],moj[i],ofj[i]});
                }

                sub.removeItemAt(idx);

                jTable1.setModel(model);

            }
            if(sub.getItemCount()==0)
            {
                jButton3.setVisible(true);
                JOptionPane.showMessageDialog(null, "Subject list are now empty");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void rollnoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_rollnoFocusLost
        // TODO add your handling code here:
        rollno.setEditable(false);
        String qry="select name,class,semester,photo,subjects  from  studentsubjectsselected where rollno='"+rollno.getText().toString()+"' && class ='"+dept+"'";
        ResultSet rs=obj.connection2(qry);
        try
        {
            int a=0;
            String sub="";
            while(rs.next())
            {
                a++;
                nme.setText(rs.getString(1));
                sem.setText(rs.getString(3));
                cls.setText(rs.getString(2));
                photo.setIcon(new ImageIcon(rs.getString(4)));
                h=rs.getString(4);
                sub=rs.getString(5).toString();

            }

            String n[]=sub.split(",");
            this.sub.removeAllItems();
            for(int i=0;i<n.length;i++)
            {

                this.sub.addItem(n[i]);
            }

        }
        catch (Exception e)
        {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_rollnoFocusLost

