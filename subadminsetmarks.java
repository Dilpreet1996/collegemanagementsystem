    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String subject="";
        String marksobtained="";
        String outof="";

        String qry="select rollno,semester,class from setmarks where rollno='"+rollno.getText().toString()+"'";
        ResultSet rs=obj.connection2(qry);
        try
        {
            int b=0;
            while(rs.next())
            {
                b++;
            }
            if(b==0)
            {
                for(int a=0;a<jTable1.getRowCount();a++)
                {
                    subject=subject+jTable1.getValueAt(a,0)+",";
                    marksobtained=marksobtained+jTable1.getValueAt(a, 1)+",";
                    outof=outof+jTable1.getValueAt(a, 2)+",";
                }
                qry="";
                qry="insert  into setmarks(rollno,name,semester,photo,class,subject,marksobtained,outof) values ('"+rollno.getText()+"','"+nme.getText()+"','"+sem.getText()+"','"+h+"','"+cls.getText()+"','"+subject+"','"+marksobtained+"','"+outof+"')";
                obj.connection(qry);
                JOptionPane.showMessageDialog(null, "Data Sucessfully Registered");
            }
            if(b>0)
            {
                JOptionPane.showMessageDialog(null, "This data is already inserted");
            }
        }
        catch (Exception e)
        {

            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

